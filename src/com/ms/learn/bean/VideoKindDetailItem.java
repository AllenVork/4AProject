package com.ms.learn.bean;

public class VideoKindDetailItem {
	private String videoId;
	private String videoName;
	private String videoChangeTime;
	private String videoDesc;
	private String videoImagUrl;
	
	public String getVideoId() {
		return videoId;
	}
	public void setVideoId(String videoId) {
		this.videoId = videoId;
	}
	public String getVideoName() {
		return videoName;
	}
	public void setVideoName(String videoName) {
		this.videoName = videoName;
	}
	public String getVideoChangeTime() {
		return videoChangeTime;
	}
	public void setVideoChangeTime(String videoChangeTime) {
		this.videoChangeTime = videoChangeTime;
	}
	public String getVideoDesc() {
		return videoDesc;
	}
	public void setVideoDesc(String videoDesc) {
		this.videoDesc = videoDesc;
	}
	public String getVideoImagUrl() {
		return videoImagUrl;
	}
	public void setVideoImagUrl(String videoImagUrl) {
		this.videoImagUrl = videoImagUrl;
	}
	 
	

}
