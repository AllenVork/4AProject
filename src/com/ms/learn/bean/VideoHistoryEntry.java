package com.ms.learn.bean;

public class VideoHistoryEntry {
	
	private String videoID;
	private String videoName;
	private String videoHadSeeTime;
	private String videoWhen;
	private String video_Descri;
	private String videoVid;
	public String getVideoID() {
		return videoID;
	}
	public void setVideoID(String videoID) {
		this.videoID = videoID;
	}
	public String getVideoName() {
		return videoName;
	}
	public void setVideoName(String videoName) {
		this.videoName = videoName;
	}
	public String getVideoHadSeeTime() {
		return videoHadSeeTime;
	}
	public void setVideoHadSeeTime(String videoHadSeeTime) {
		this.videoHadSeeTime = videoHadSeeTime;
	}
	public String getVideoWhen() {
		return videoWhen;
	}
	public void setVideoWhen(String videoWhen) {
		this.videoWhen = videoWhen;
	}
	public String getVideo_Descri() {
		return video_Descri;
	}
	public void setVideo_Descri(String video_Descri) {
		this.video_Descri = video_Descri;
	}
	public String getVideoUrl() {
		return videoVid;
	}
	public void setVideoUrl(String videoUrl) {
		this.videoVid = videoUrl;
	}
	
	

}
